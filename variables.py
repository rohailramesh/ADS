# python variables are dynamically type meaning their type is decided at run time
a = 0
print(a)
# at run time, the type will be determined for a as an integer and 0 will be printed

a = "abc"
print(a)
# can re-assign the same variable which was integer ealier to another type and at run time, it will be set as a string now

# possible to assign value to different variable with different types on the same line
x,y,z = 'a',1,1.5
print(x,y,z)

# python uses 'None' instead of null